    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String query="";
        if (roleCb.getSelectedItem().toString().equals("ADMINISTRATEUR")) {
            query = "select * from admintbl where name='"+userLbl.getText()+"' and password='"+pwdLbl.getText()+"'";
        }else{
            query = "select * from selertbl where name='"+userLbl.getText()+"' and password='"+pwdLbl.getText()+"'";   
        }
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_supermarket","root","");
            st = con.createStatement();
            rs= st.executeQuery(query);
            if (rs.next()) {
                if(roleCb.getSelectedItem().toString().equals("ADMINISTRATEUR")){
                    new Products().setVisible(true);
                }else{
                    new Seling().setVisible(true);
                }
                this.dispose();
            }else {
                JOptionPane.showMessageDialog(this, "L'utilisateur ou role ou mot de passe incorrect", null, JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1MouseClicked

